// penjelasan kenapa var sudah tidak di recomendasikan
/*
var tidak lagi disarankan untuk mendeklarasikan
variabel di JavaScript modern karena perilaku
hoisting dan function scope. Variabel yang
dideklarasikan dengan var memiliki cakupan
fungsi atau cakupan global, dan terlihat
melalui blok. Var mengabaikan blok kode,
sehingga variabel global akan tetap ada. 
*/
const name = "abror";
